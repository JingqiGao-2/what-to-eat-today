export const allDishNames = foods.map((food) => food.name);

// 按菜系分类
export const foodsByCuisine = foods.reduce(
  (acc, food) => {
    if (!acc[food.cuisine]) {
      acc[food.cuisine] = [];
    }
    acc[food.cuisine].push(food);
    return acc;
  },
  {} as Record<string, FoodItem[]>,
);

// 按标签分类
export const foodsByTag = foods.reduce(
  (acc, food) => {
    food.tags.forEach((tag) => {
      if (!acc[tag]) {
        acc[tag] = [];
      }
      acc[tag].push(food);
    });
    return acc;
  },
  {} as Record<string, FoodItem[]>,
);

